// Hold the jelly jar.
hold(jellyJar);
// Open the jelly jar.
open(jellyJar);
// Scoop jelly from the jelly jar.
scoop(jellyFromJellyJar); // The correct term is scoop(jelly);
// Put the jelly jar down.
put(jellyJarDown); // The correct term is put(jellyJar);
// Steady the right slice. 
steady(rightSlice);
// Spread the jelly on the right slice.
spread(jellyOnRightSlice); // The correct term is spread(jelly);
